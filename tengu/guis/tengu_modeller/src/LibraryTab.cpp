#include "LibraryTab.h"

// ********************************************************************************************************************
// *                                                                                                                  *
// *                                                  The constructor.                                                *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                                                    Конструктор.                                                  *
// *                                                                                                                  *
// ********************************************************************************************************************

tengu::LibraryTab::LibraryTab ( QWidget* parent ) 
    : QTabWidget ( parent ) 
{
    setTabPosition( QTabWidget::South );
    setTabShape( QTabWidget::Triangular );
    
    QFont font("Tahoma", 8 );
    setFont( font );
    
    tab__processes = new LibraryTab__processes();
    addTab( tab__processes, tr("Processes") );
}

// ********************************************************************************************************************
// *                                                                                                                  *
// *                                            We are inside of a process.                                           *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                                                Мы - внутри процесса.                                             *
// *                                                                                                                  *
// ********************************************************************************************************************

void tengu::LibraryTab::on__inside_process() {
    
    tab__processes->setEnabled( true );
    setCurrentWidget( tab__processes );
    
}

