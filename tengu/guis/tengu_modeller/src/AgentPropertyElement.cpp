#include "AgentPropertyElement.h"

// ********************************************************************************************************************
// *                                                                                                                  *
// *                                                The constructor.                                                  *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                                                   Конструктор.                                                   *
// *                                                                                                                  *
// ********************************************************************************************************************

tengu::AgentPropertyElement::AgentPropertyElement() {
    
    propertyName = QString("");
    value = QVariant(QString(""));
    font = QFont("Arial", 9 );
    readOnly = true;
    backgroundColor = baseBackground();
    // editorWidget = nullptr;
    type = AgentPropertyElement::String;
}

// ********************************************************************************************************************
// *                                                                                                                  *
// *                                         The constructor with a parameters.                                       *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                                              Конструктор от параметорв.                                          *
// *                                                                                                                  *
// ********************************************************************************************************************

tengu::AgentPropertyElement::AgentPropertyElement ( QString text ) 
    : AgentPropertyElement()
{
    value = QVariant( text );
}

// ********************************************************************************************************************
// *                                                                                                                  *
// *                                          Get background color for a widget.                                      *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                                              Вернуть цвет фона виджита.                                          *
// *                                                                                                                  *
// ********************************************************************************************************************

QColor tengu::AgentPropertyElement::widgetBackground() {
    QWidget w;
    return w.palette().color( QPalette::Window );
}

// ********************************************************************************************************************
// *                                                                                                                  *
// *                                          Return the base background color.                                       *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                                             Вернуть основной цвет фона.                                          *
// *                                                                                                                  *
// ********************************************************************************************************************

QColor tengu::AgentPropertyElement::baseBackground() {
    QWidget w;
    return w.palette().color( QPalette::Base );
}

// ********************************************************************************************************************
// *                                                                                                                  *
// *                              Get background color for disabled for editing element.                              *
// * ---------------------------------------------------------------------------------------------------------------- *
// *                        Вернуть цвет фона для запрещенного для редактирования элемента.                           *
// *                                                                                                                  *
// ********************************************************************************************************************

QColor tengu::AgentPropertyElement::disabledBackground() {
    QWidget w;
    return w.palette().color( QPalette::Button );
}

